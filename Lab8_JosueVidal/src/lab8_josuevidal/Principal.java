    private void jButton8MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton8MouseClicked
        // TODO add your handling code here:
         DefaultMutableTreeNode n = (DefaultMutableTreeNode) jTree1.getLastSelectedPathComponent();
        Proyecto temp = null;
        for (Proyecto P : Proyectos) {
            if (P.getNombre().equals(n.toString())) {
                temp = P;
            }
        }
        Proyectos.remove(temp);
        DefaultComboBoxModel m = (DefaultComboBoxModel) cb_proyectos.getModel();
        m.removeElement(temp);
        jTree1.setModel(new DefaultTreeModel(new DefaultMutableTreeNode("")));

    }//GEN-LAST:event_jButton8MouseClicked

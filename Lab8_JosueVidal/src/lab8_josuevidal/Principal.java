    ArrayList<Actividades> activities = new ArrayList();

    public void tirar_arreglo(ArrayList<Actividades> p, DefaultMutableTreeNode nodo) {
        try {

            for (Actividades temp : p) {

                if (temp.getPredecesoras().isEmpty()) {
                    boolean ver = false;
                    for (Actividades t : activities) {
                        if (temp.getNombre().equals(t.getNombre())) {
                            ver = true;
                        }

                    }
                    if (!ver) {
                        activities.add(temp);

                    }

                } else {

                    DefaultMutableTreeNode n = new DefaultMutableTreeNode(temp.getNombre());
                    nodo.add(n);
                    boolean ver = false;
                    for (Actividades t : activities) {
                        if (temp.getNombre().equals(t.getNombre())) {
                            ver = true;
                        }

                    }
                    if (!ver) {
                        activities.add(temp);

                    }
                    listar_tree(temp.getPredecesoras(), n);

                }

            }
        } catch (Exception e) {
        }
    }

    public void listar_tree(ArrayList<Actividades> p, DefaultMutableTreeNode nodo) {
        try {

            for (Actividades temp : p) {

                if (temp.getSucesoras().isEmpty() && temp.getPredecesoras().size() < 1) {
                    DefaultMutableTreeNode n = new DefaultMutableTreeNode(temp.getNombre());
                    nodo.add(n);
                    System.out.println("hice");

                } else {

                    DefaultMutableTreeNode n = new DefaultMutableTreeNode(temp.getNombre());
                    nodo.add(n);
                    System.out.println("hiceeldos");
                    listar_tree(temp.getSucesoras(), n);

                }

            }
        } catch (Exception e) {
        }

    }


